<B>Forum Codes</B><BR>
<DIV ALIGN=JUSTIFY>
Forum codes are used to replace HTML tags within forum posts, allowing users to add images, links, and text styles, while giving increased security to the board itself
<P>
Please note that forum codes are NOT case sensetive.
